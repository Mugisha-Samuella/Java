package Basics1;

public class RelationalOperators {
    public static void main(String[] args){
        int number1 = 8;
        int number2 = 4;

        //is equal to
        System.out.println(number1 == number2); //false

        //is not equal to
        System.out.println(number1 != number2); //true

        //is greater than
        System.out.println(number1 > number2); //true

        //is less than
        System.out.println(number1 < number2); //false

        //is greater than or equal to
        System.out.println(number1 >= number2); //true

        //is less than or equal to
        System.out.println(number1 <= number2); //false
    }
}
