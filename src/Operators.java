public class Operators{
    public static void main(String[] args){
        int number1 = 15;
        int number2 = 10;

        //addition
        System.out.println(number1 + number2); //25

        //subtraction
        System.out.println(number1 - number2); //5
        System.out.println(number2 - number1); //-5

        //multiplication
        System.out.println(number1 * number2); //150

        //division
        System.out.println(number1 / number2); //1


        //modulo
        System.out.println(number1 % number2); //5
    }
}
